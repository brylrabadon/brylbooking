    private void search_fieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_search_fieldKeyTyped
    // Use the current text plus the character currently being typed
    String query = search_field.getText() + evt.getKeyChar();
    
    // Check if the query is practically empty (handling backspaces/empty input)
    if (query.trim().isEmpty()) {
        displayData(); 
        return;
    }

    String sql = "SELECT account_id AS 'ID', email AS 'Email', "
               + "contact AS 'Contact', username AS 'Username', "
               + "role AS 'Role', account_status AS 'Status' "
               + "FROM accounts WHERE "
               + "account_id LIKE '%" + query + "%' OR "
               + "contact LIKE '%" + query + "%' OR "
               + "username LIKE '%" + query + "%' OR "
               + "email LIKE '%" + query + "%' OR "
               + "role LIKE '%" + query + "%'";
               
    displayData(sql); 
    }//GEN-LAST:event_search_fieldKeyTyped

