    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        int rowIndex = accounts_table.getSelectedRow();

        if (rowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Please select an item to delete!");
        } else {
            int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this guest?", "Warning", JOptionPane.YES_NO_OPTION);
            
            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    TableModel model = accounts_table.getModel();
                    String id = model.getValueAt(rowIndex, 0).toString();
                    
                    config conf = new config();
                    // Note: Ensure your config.java has the deleteData method
                    int result = conf.deleteData("DELETE FROM accounts WHERE account_id = '" + id + "'");
                    
                    if(result == 1){
                        JOptionPane.showMessageDialog(null, "Deleted Successfully!");
                        displayData(); // Refresh the table
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error deleting: " + e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jLabel2MouseClicked

