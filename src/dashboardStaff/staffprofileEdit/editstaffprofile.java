    private void jLabel9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9MouseClicked
    session sess = session.getInstance();
    config db = new config();

    String firstName = sfname.getText();
    String lastName  = slname.getText();
    String userEmail = semail.getText();   // Matches your variable 'sname' for email
    String userCont  = scontact.getText();
    String userUname = suser.getText();
    String rawPass   = spass.getText();

    try {
        String updateQuery;
        
        // Use hashing if a new password is provided
        if (!rawPass.isEmpty() && !rawPass.equals("********")) {
            String hashedPass = passwordhashed.hashPassword(rawPass);
            updateQuery = "UPDATE accounts SET first_name = '" + firstName + "', "
                        + "last_name = '" + lastName + "', email = '" + userEmail + "', "
                        + "contact = '" + userCont + "', username = '" + userUname + "', "
                        + "password = '" + hashedPass + "' "
                        + "WHERE account_id = " + sess.getAccountId();
        } else {
            updateQuery = "UPDATE accounts SET first_name = '" + firstName + "', "
                        + "last_name = '" + lastName + "', email = '" + userEmail + "', "
                        + "contact = '" + userCont + "', username = '" + userUname + "' "
                        + "WHERE account_id = " + sess.getAccountId();
        }

        if (db.executeUpdate(updateQuery) > 0) {
            javax.swing.JOptionPane.showMessageDialog(null, "Staff Profile Updated Successfully!");

            // Sync the session
            sess.setFname(firstName);
            sess.setLname(lastName);
            sess.setEmail(userEmail);
            sess.setContact(userCont);
            sess.setUsername(userUname);
            sess.setPass(rawPass);

            // Redirect back to the Staff Dashboard
            new dashboardStaff.staffprofileEdit.staffprofile().setVisible(true); 
            this.dispose();
        }
    } catch (Exception e) {
        System.out.println("Staff Update Error: " + e.getMessage());
    }

    }//GEN-LAST:event_jLabel9MouseClicked

